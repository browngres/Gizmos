def find_differences(x, y):
    # 将数组转换为集合
    set_x = set(x)
    set_y = set(y)

    # 找出 x 有而 y 没有的元素
    only_in_x = set_x - set_y
    # 找出 y 有而 x 没有的元素
    only_in_y = set_y - set_x

    # 输出结果
    print(f"贪婪 有 {', '.join(map(str, sorted(only_in_x)))};")
    print(f"答案 有 {', '.join(map(str, sorted(only_in_y)))}")


x = [1, 2, 3, 4, 5, 6, 8, 9, 10, 11, 14, 15, 16, 17, 18, 21, 24, 25, 27, 29, 30, 31, 32, 37, 38, 40, 41, 42, 44, 45, 47, 48, 50, 51, 54, 57, 58]
y = [1, 2, 3, 4, 5, 6, 8, 9, 10, 11, 14, 16, 17, 18, 19, 21, 24, 25, 27, 29, 30, 31, 32, 37, 38, 40, 41, 42, 44, 45, 47, 48, 50, 51, 54, 57, 58]
find_differences(x,y)
